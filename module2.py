# Ice Cream Price Checker

# Define Ice cream prices
Vanilla_ice_cream = 25
Chocolate_ice_cream = 37

# Check which Ice cream is less expensive
if Vanilla_ice_cream < Chocolate_ice_cream:
    print("Vanilla Ice cream is less expensive than Chocolate Ice cream.")
elif Chocolate_ice_cream < Vanilla_ice_cream:
    print("Chocolate Ice cream is less expensive than Vanilla Ice cream.")
else:
    print("Both Ice creams have the same price.")


